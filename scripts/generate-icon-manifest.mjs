import { readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const frameworkRoot = path.resolve(scriptDir, '..');
const svgDir = path.join(frameworkRoot, 'icons', 'svg');
const manifestPath = path.join(frameworkRoot, 'icons', 'manifest.ts');

const ATTRIBUTE_MAP = {
  'stroke-width': 'strokeWidth',
  'stroke-dasharray': 'strokeDasharray',
  'stroke-linecap': 'strokeLinecap',
  'stroke-linejoin': 'strokeLinejoin',
  'fill-rule': 'fillRule',
  'clip-rule': 'clipRule',
};

function parseAttributes(source) {
  const attrs = {};
  for (const match of source.matchAll(/([\w:-]+)="([^"]*)"/g)) {
    const [, rawKey, value] = match;
    attrs[ATTRIBUTE_MAP[rawKey] ?? rawKey] = value;
  }
  return attrs;
}

function normalizeStrokeWidth(value) {
  if (value === undefined) return undefined;
  const numeric = Number(value);
  return Number.isFinite(numeric) && String(numeric) === value ? numeric : value;
}

function parseIcon(name, source) {
  const svgMatch = source.match(/<svg\b([^>]*)>/);
  if (!svgMatch) {
    throw new Error(`${name}.svg is missing an <svg> root`);
  }

  const svgAttrs = parseAttributes(svgMatch[1]);
  const viewBox = svgAttrs.viewBox;
  if (!viewBox) {
    throw new Error(`${name}.svg is missing a viewBox`);
  }

  const nodes = [];
  for (const match of source.matchAll(/<path\b([^>]*)\/?>/g)) {
    const attrs = parseAttributes(match[1]);
    if (!attrs.d) {
      throw new Error(`${name}.svg has a <path> without d`);
    }
    const node = {
      d: attrs.d,
      stroke: attrs.stroke ?? svgAttrs.stroke,
      fill: attrs.fill ?? svgAttrs.fill,
      strokeWidth: normalizeStrokeWidth(attrs.strokeWidth ?? svgAttrs.strokeWidth),
      strokeDasharray: attrs.strokeDasharray,
      strokeLinecap: attrs.strokeLinecap ?? svgAttrs.strokeLinecap,
      strokeLinejoin: attrs.strokeLinejoin ?? svgAttrs.strokeLinejoin,
      fillRule: attrs.fillRule,
      clipRule: attrs.clipRule,
    };
    nodes.push(Object.fromEntries(Object.entries(node).filter(([, value]) => value !== undefined)));
  }

  if (nodes.length === 0) {
    throw new Error(`${name}.svg has no <path> nodes`);
  }

  return { name, viewBox, nodes };
}

function renderManifest(icons) {
  const iconNames = icons.map(icon => icon.name);
  const typeLines = iconNames.map(name => `  | ${JSON.stringify(name)}`).join('\n');
  return `// This file is generated by scripts/generate-icon-manifest.mjs.
// Do not edit manually.

export interface CxIconNode {
  d: string;
  stroke?: string;
  fill?: string;
  strokeWidth?: number | string;
  strokeDasharray?: string;
  strokeLinecap?: 'round' | 'square' | 'butt';
  strokeLinejoin?: 'round' | 'miter' | 'bevel';
  fillRule?: 'evenodd' | 'nonzero';
  clipRule?: 'evenodd' | 'nonzero';
}

export interface CxIconDefinition {
  name: string;
  viewBox: string;
  nodes: CxIconNode[];
}

export type CxIconName =
${typeLines}
;

export const cxIcons: CxIconDefinition[] = ${JSON.stringify(icons, null, 2)};

const cxIconMap = new Map<CxIconName, CxIconDefinition>(
  cxIcons.map((icon) => [icon.name as CxIconName, icon]),
);

export function getCxIcon(name: CxIconName | null | undefined): CxIconDefinition | null {
  return name ? cxIconMap.get(name) ?? null : null;
}
`;
}

async function generateIconManifest() {
  const files = (await readdir(svgDir)).filter(file => file.endsWith('.svg')).sort();
  const icons = [];
  for (const file of files) {
    const name = file.slice(0, -'.svg'.length);
    const source = await readFile(path.join(svgDir, file), 'utf8');
    icons.push(parseIcon(name, source));
  }
  await writeFile(manifestPath, renderManifest(icons));
  console.log(`Generated ${icons.length} icons at ${path.relative(process.cwd(), manifestPath)}`);
}

generateIconManifest().catch(error => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exitCode = 1;
});
